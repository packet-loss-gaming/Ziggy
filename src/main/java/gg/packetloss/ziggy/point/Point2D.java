package gg.packetloss.ziggy.point;

public class Point2D {
    private final int x;
    private final int z;

    public Point2D(int x, int z) {
        this.x = x;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getZ() {
        return z;
    }

    public double distanceSquared(Point2D other) {
        return Math.pow(getX() - other.getX(), 2) + Math.pow(getZ() - other.getZ(), 2);
    }
}
